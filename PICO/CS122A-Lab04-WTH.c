const unsigned int sprite_size = sizeof(sprite) / sizeof(sprite[0]);

int main()
{
    //SPI init
    spi_init(SPI_PORT, 1000*1000);  //1Mhz
    gpio_set_function(PIN_SCK,  GPIO_FUNC_SPI);
    gpio_set_function(PIN_MOSI, GPIO_FUNC_SPI);
    
    //CS init
    gpio_init(PIN_CS);
    gpio_set_dir(PIN_CS, GPIO_OUT);
    gpio_put(PIN_CS, 1);    //idle high

    uint8_t bytes[2];

    while (true) 
    {
        //write SPI
        gpio_put(PIN_CS, 0);

        for (int i = 0; i < sprite_size; i++)
        {
            uint16_t pixel = sprite[i];

            bytes[0] = (pixel >> 8) & 0xFF; // high byte
            bytes[1] = pixel & 0xFF;        // low byte
            spi_write_blocking(SPI_PORT, bytes, 2);
        }

        gpio_put(PIN_CS, 1);
    }
}
